// This code snippet is TypeScript without React.

// Function that takes two numbers (a and b) and returns their sum.
function add(a: number, b: number): number {
  return a + b;
}

// Calling the add function with arguments 2 and 5, and storing the result in the 'result' variable.
const result: number = add(2, 5);

// Outputting the value stored in 'result' to the console.
console.log(result);
